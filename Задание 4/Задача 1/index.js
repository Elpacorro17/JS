// Функция для вычисления среднего арифметического
function average(num1, num2, num3) {
    return (num1 + num2 + num3) / 3;
}

// Примеры вызова функции с разными значениями
console.log(`Среднее арифметическое: ${average(1, 2, 3)}`); // 2
console.log(`Среднее арифметическое: ${average(10, 20, 30)}`); // 20
console.log(`Среднее арифметическое: ${average(-5, 0, 5)}`); // 0
console.log(`Среднее арифметическое: ${average(4.5, 5.5, 6.5)}`); // 5.5
